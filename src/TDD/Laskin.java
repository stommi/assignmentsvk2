package TDD;

public class Laskin {
    public int plussaa(int x, int y) {
        return x + y;
    }

    public int miinusta(int x, int y) {
        return x - y;
    }

    public int kerro(int x, int y) {
        return x*y;
    }

    public double jaa(int jaettava, int jakaja) {
        return jaettava/jakaja*1.0;
    }
}
